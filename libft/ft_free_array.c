#include "libft.h"

void	ft_free_array(char ***str)
{
	size_t	i;

	i = 0;
	if (*str)
	{
		while ((*str)[i] != NULL)
			free((*str)[i++]);
		free(*str);
		*str = NULL;
	}
}
